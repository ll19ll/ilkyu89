package model;

public class BarChartVO {
private String pay;
private String mon;


public BarChartVO() {
	super();
}

public BarChartVO(String pay, String mon) {
	super();
	this.pay = pay;
	this.mon = mon;
}

public String getPay() {
	return pay;
}

public void setPay(String pay) {
	this.pay = pay;
}

public String getMon() {
	return mon;
}

public void setMon(String mon) {
	this.mon = mon;
}




}
